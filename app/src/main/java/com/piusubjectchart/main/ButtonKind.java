package com.piusubjectchart.main;

/**
 * メイン画面のボタンの種類を表す列挙型
 */
enum ButtonKind {
    /**
     * 「ステップ」のボタン
     */
    STEP,

    /**
     * 「難易度」のボタン
     */
    DIFFICULTY,

    /**
     * 「種別」のボタン
     */
    TYPE,

    /**
     * 「シリーズ」のボタン
     */
    SERIES,

    /**
     * 「カテゴリー」のボタン
     */
    CATEGORY,

    /**
     * 「お題を出す」のボタン
     */
    RUN,
}
